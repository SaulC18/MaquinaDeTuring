    public void cambio(ArrayList<String> a){
        int x = a.size();
        int aux = pos;
        System.out.print("( ");
        for (int i = 0; i < x; i++) {
            if(dire == 1){
                if(i == (aux-1)){
                    if(estado != 8){
                        if(!(ant.equals(nue))){
                            System.out.print(RED+a.get(i)+RESET+" ");
                            i++;
                            if(i != x){
                                System.out.print(GREEN+a.get(i)+RESET+" ");
                            }
                        }else{
                            System.out.print(a.get(i)+" ");
                            i++;
                            System.out.print(GREEN+a.get(i)+RESET+" ");
                        }
                    }else{
                        System.out.print(a.get(i)+" ");
                    }
                }else{
                    System.out.print(a.get(i)+" ");
                }
            }else{
                if(i == aux){
                    if(!(ant.equals(nue))){
                        System.out.print(GREEN+a.get(i)+RESET+" ");
                        i++;
                        System.out.print(RED+a.get(i)+RESET+" ");
                    }else{
                        System.out.print(GREEN+a.get(i)+RESET+" ");
                        i++;
                        System.out.print(a.get(i)+" ");
                    }
                }else{
                    System.out.print(a.get(i)+" ");
                }
            }
        }
        System.out.print(")");
    }
    
