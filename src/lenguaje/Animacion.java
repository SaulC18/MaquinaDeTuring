    private void btnComprobarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnComprobarActionPerformed
        String pal = tfPalabra.getText();
        int x = pal.length();
        System.out.println("Cantidad de letras: "+x);
        System.out.println("Palabra : "+pal);
        estIni = 0;
        estado = 0;
        pos = 0;
        int contador = 0;
        if(x != 0){
            ArrayList<String> letras;
            letras = new ArrayList<String>(Arrays.asList(pal.split("")));
            letras.add("D");
            for (int i = 0; i < letras.size()-1; i++) {
                if(letras.get(i).equals("a") || letras.get(i).equals("b")){
                    contador++;
                }else{
                    JOptionPane.showMessageDialog(this, "Lo siento, solo aceptamos 'a' o 'b'");
                    tfPalabra.setText("");
                    estado = 8;
                    break;
                }
            }
            if(contador == (letras.size()-1)){
                System.out.println(letras+"\tPosicion: "+pos+"\tInicial: "+estIni+"\tFinal: "+estado);
                compPali(letras);
                tfPalabra.setText("");
                System.out.println("\n");
            }
        }else{
            JOptionPane.showMessageDialog(this, "Debe de ingresar al menos una letra");
        }
    }//GEN-LAST:event_btnComprobarActionPerformed
    
    public void compPali(ArrayList<String> a){
        int x = a.size(), cont2 = 0;
        while(estado != 8){
            estIni = estado;
            System.out.print(COLOR+"Regla "+RESET+"("+estado+","); 
            if(estado == 0){
                regI(a,a.get(pos));
            }else if(estado == 1){
                regE1(a,a.get(pos));
            }else if(estado == 2){
                regE2(a,a.get(pos));
            }else if(estado == 3){
                regE3(a,a.get(pos));
            }else if(estado == 4){
                regE4(a,a.get(pos));
            }else if(estado == 5){
                regE5(a,a.get(pos));
            }else if(estado == 6){
                regE6(a,a.get(pos));
            }else if(estado == 7){
                regE7(a,a.get(pos));
            }
            System.out.print("\t");
            cambio(a);
            System.out.print(RED+"\t Cambio: "+RESET+ant+" - "+nue);
            System.out.println(GREEN+"\tPosicion Actual: "+RESET+pos+COLOR+"\tInicial: "+RESET+estIni+COLOR+"\tFinal: "+RESET+estado+"\n");
            if(cont == 3){
                cont = 0;
                break;
            }
        }
        for (int i = 0; i < x-1; i++) {
            if(a.get(i).equals("L") || a.get(i).equals("B")){
                cont2++;
            }else{
                JOptionPane.showMessageDialog(this, "Lo siento pero no es un palindromo :(");
                tfPalabra.setText("");
                break;
            }
        }
        if(cont2 == x-1){
            JOptionPane.showMessageDialog(this, "Felicidades, usted ingreso un palíndromo :)");
        }
    }
    
